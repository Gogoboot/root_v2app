// root-identity/src/constants.rs

/// Системный префикс для BIP39 деривации ключей.
///
/// Добавляется перед пользовательской passphrase.
/// Защищает от коллизий с другими BIP39 приложениями
/// которые могут использовать ту же мнемонику.
pub const BIP39_PREFIX: &str = "ROOT_v2_";

/// Индекс деривации для основного аккаунта.
///
/// Зарезервирован для будущей поддержки BIP32
/// (несколько аккаунтов из одной мнемоники).
/// Сейчас всегда 0 — один аккаунт.
///
/// Будущее использование:
///   0 → основной аккаунт
///   1 → резервный аккаунт
///   N → дополнительные аккаунты
pub const DERIVATION_INDEX: u32 = 0;
