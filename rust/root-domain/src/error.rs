//! Единый тип ошибок доменного слоя.
//!
//! [`DomainError`] — это "язык бизнеса". Все остальные крейты
//! (root-storage, root-crypto, root-network) говорят на своих языках,
//! но на FFI-границе всё переводится в [`DomainError`].
//!
//! # Правило
//!
//! `root-domain` не знает про `rusqlite::Error`, `CryptoError` и т.д.
//! Конвертация происходит в соответствующих крейтах (S6-F5),
//! а не здесь.

use thiserror::Error;

/// Все ошибки которые может вернуть бизнес-логика RooT Messenger.
///
/// Этот тип — единственный который видит `root-ffi`.
/// Инфраструктурные ошибки (SQLite, крипто, сеть) конвертируются
/// в него через `From` impl в своих крейтах.
///
/// # Варианты и когда они возникают
///
/// | Вариант              | Когда возникает                              |
/// |----------------------|----------------------------------------------|
/// | `Storage`            | Не удалось прочитать/записать данные         |
/// | `Crypto`             | Шифрование/дешифрование провалилось          |
/// | `Network`            | Проблемы с P2P соединением                   |
/// | `Validation`         | Пользователь ввёл некорректные данные        |
/// | `NotFound`           | Запрошенная сущность отсутствует             |
/// | `InvalidState`       | Приложение в неожиданном состоянии           |
/// | `PermissionDenied`   | Операция запрещена (например после паники)   |
#[derive(Error, Debug)]
pub enum DomainError {

    // ─── Инфраструктурные ошибки (приходят снаружи) ──────────────────────

    /// Сбой при работе с хранилищем данных.
    ///
    /// Строка внутри — человекочитаемое описание.
    /// Конкретный тип ошибки (rusqlite, sled и т.д.) скрыт намеренно:
    /// если завтра сменить БД, этот вариант останется прежним.
    #[error("Ошибка хранилища: {0}")]
    Storage(String),

    /// Сбой криптографической операции.
    ///
    /// Может означать: неверный ключ, повреждённые данные,
    /// ошибку алгоритма шифрования.
    #[error("Ошибка криптографии: {0}")]
    Crypto(String),

    /// Сбой сетевого слоя (libp2p, QUIC, DHT).
    ///
    /// Строка внутри — описание от сетевого крейта,
    /// преобразованное через `.to_string()`.
    #[error("Ошибка сети: {0}")]
    Network(String),

    // ─── Бизнес-ошибки (логика приложения) ───────────────────────────────

    /// Входные данные не прошли проверку.
    ///
    /// Используется когда пользователь ввёл некорректное значение.
    /// Содержит имя поля и описание проблемы — удобно показывать в UI.
    ///
    /// # Пример
    ///
    /// ```rust
    /// return Err(DomainError::Validation {
    ///     field: "nickname".to_string(),
    ///     message: "не может быть пустым".to_string(),
    /// });
    /// ```
    #[error("Ошибка валидации поля '{field}': {message}")]
    Validation {
        /// Имя поля которое не прошло проверку (например: "nickname", "topic")
        field: String,
        /// Описание что именно не так
        message: String,
    },

    /// Запрошенная сущность не найдена.
    ///
    /// `entity` — тип сущности, `id` — её идентификатор.
    /// Оба поля попадают в сообщение об ошибке автоматически.
    ///
    /// # Пример
    ///
    /// ```rust
    /// return Err(DomainError::NotFound {
    ///     entity: "Message",
    ///     id: msg_id.to_string(),
    /// });
    /// ```
    #[error("{entity} с id '{id}' не найден")]
    NotFound {
        /// Название типа сущности: "Message", "Contact", "Account"
        /// `'static str` — это строка известная на этапе компиляции,
        /// не требует выделения памяти в куче.
        entity: &'static str,
        /// Строковое представление идентификатора
        id: String,
    },

    /// Приложение находится в состоянии при котором операция невозможна.
    ///
    /// Например: попытка отправить сообщение до инициализации аккаунта,
    /// или обратиться к БД после активации Panic Button.
    #[error("Неверное состояние приложения: {0}")]
    InvalidState(String),

    /// Операция запрещена для текущего пользователя или состояния.
    ///
    /// Отличие от `InvalidState`: здесь состояние корректное,
    /// но конкретное действие не разрешено (например: чужое сообщение).
    #[error("Доступ запрещён: {0}")]
    PermissionDenied(String),
}

// ─── Вспомогательные методы ──────────────────────────────────────────────────

impl DomainError {
    /// Возвращает стабильный строковый код ошибки для FFI-границы.
    ///
    /// Flutter-клиент использует эти коды чтобы показать
    /// правильное сообщение на нужном языке интерфейса.
    /// Коды стабильны между версиями — не переименовывай их.
    ///
    /// # Пример
    ///
    /// ```rust
    /// let err = DomainError::NotFound {
    ///     entity: "Contact",
    ///     id: "abc".into(),
    /// };
    /// assert_eq!(err.code(), "domain.not_found");
    /// ```
    pub fn code(&self) -> &'static str {
        match self {
            DomainError::Storage(_)         => "domain.storage",
            DomainError::Crypto(_)          => "domain.crypto",
            DomainError::Network(_)         => "domain.network",
            // Validation и NotFound — именованные поля, поэтому { .. }
            // а не просто (_). Нам содержимое не нужно, поэтому ..
            DomainError::Validation { .. }  => "domain.validation",
            DomainError::NotFound { .. }    => "domain.not_found",
            DomainError::InvalidState(_)    => "domain.invalid_state",
            DomainError::PermissionDenied(_)=> "domain.permission_denied",
        }
    }

    /// Конструктор для удобного создания ошибки валидации.
    ///
    /// Без этого метода каждый раз нужно писать громоздкую конструкцию
    /// с именованными полями. Метод сокращает код.
    ///
    /// # Пример
    ///
    /// ```rust
    /// // Вместо:
    /// DomainError::Validation { field: "nickname".into(), message: "пусто".into() }
    ///
    /// // Пишем:
    /// DomainError::validation("nickname", "пусто")
    /// ```
    pub fn validation(field: impl Into<String>, message: impl Into<String>) -> Self {
        DomainError::Validation {
            field: field.into(),
            message: message.into(),
        }
    }

    /// Конструктор для удобного создания ошибки "не найдено".
    ///
    /// # Пример
    ///
    /// ```rust
    /// DomainError::not_found("Message", &msg_id)
    /// ```
    pub fn not_found(entity: &'static str, id: impl ToString) -> Self {
        DomainError::NotFound {
            entity,
            id: id.to_string(),
        }
    }
}
