// ── Создание нового аккаунта ──────────────────────────────────────────────────

/**
 * Создаёт новую идентичность (Ed25519 ключи + BIP-39 мнемоника).
 *
 * Правильный порядок операций:
 *   1. unlock_database() — сначала открываем БД
 *   2. generate_identity() — Rust создаёт ключи и сохраняет
 *      зашифрованную мнемонику в открытую БД
 *   3. Показываем мнемонику пользователю — она видна ОДИН РАЗ
 *   4. enterApp() — переходим в основной интерфейс
 *
 * Если открыть БД после создания identity — мнемоника не сохранится.
 */
async function generateIdentity() {
    // Читаем путь к БД и пароль — они нужны ДО создания identity
    const path     = document.getElementById('db-path').value.trim();
    const password = document.getElementById('db-password').value;

    // Без БД мнемоника не сохранится — предупреждаем пользователя
    if (!path || !password) {
        log('Сначала укажи путь к БД и пароль в блоке ниже', 'error');
        return;
    }

    try {
        // Шаг 1 — открываем зашифрованную БД
        // Rust создаст файл если его нет, или откроет существующий
        await invoke('unlock_database', { password, dbPath: path });
        log('База данных открыта', 'success');

        // Шаг 2 — генерируем Ed25519 ключи и BIP-39 мнемонику (24 слова)
        // Rust автоматически зашифрует и сохранит мнемонику в БД
        const info = await invoke('generate_identity');

        // Шаг 3 — показываем мнемонику пользователю
        // Она отображается ТОЛЬКО ОДИН РАЗ — пользователь должен её записать
        if (info.mnemonic) {
            document.getElementById('mnemonic-text').textContent = info.mnemonic;
            document.getElementById('mnemonic-display').style.display = 'block';
        }

        log(`Аккаунт создан: ${info.public_key.slice(0, 16)}...`, 'success');

        // Шаг 4 — переходим в основной интерфейс
        enterApp();

    } catch (e) {
        // e содержит текст ошибки от Rust (например: БД уже существует,
        // ошибка Argon2id, недостаточно прав на файл)
        log(`Ошибка создания аккаунта: ${e}`, 'error');
    }
}

// ── Восстановление аккаунта ───────────────────────────────────────────────────

/**
 * Восстанавливает идентичность из BIP-39 мнемоники (24 слова).
 *
 * Используется когда пользователь:
 * - Переустановил приложение
 * - Потерял файл БД
 * - Переходит на новое устройство
 *
 * Порядок операций:
 *   1. unlock_database() — открываем или создаём новую БД
 *   2. restore_identity() — восстанавливаем ключи из мнемоники
 *   3. enterApp() — переходим в основной интерфейс
 *
 * Мнемоника при восстановлении НЕ показывается повторно —
 * пользователь уже должен её знать.
 */
async function restoreIdentity() {
    // Читаем мнемонику из поля ввода
    const mnemonic = document.getElementById('restore-mnemonic').value.trim();

    // Читаем путь к БД и пароль
    const path     = document.getElementById('db-path').value.trim();
    const password = document.getElementById('db-password').value;

    // Проверяем что все поля заполнены
    if (!mnemonic) {
        log('Введи 24 слова мнемоники', 'error');
        return;
    }

    if (!path || !password) {
        log('Укажи путь к БД и пароль в блоке ниже', 'error');
        return;
    }

    try {
        // Шаг 1 — открываем или создаём БД
        await invoke('unlock_database', { password, dbPath: path });
        log('База данных открыта', 'success');

        // Шаг 2 — восстанавливаем Ed25519 ключи из мнемоники
        // Rust проверит что мнемоника валидная (24 слова BIP-39)
        // и пересохранит её зашифрованной в БД
        const info = await invoke('restore_identity', { mnemonic });

        log(`Аккаунт восстановлен: ${info.public_key.slice(0, 16)}...`, 'success');

        // Шаг 3 — переходим в основной интерфейс
        // Мнемоника при восстановлении не показывается
        enterApp();

    } catch (e) {
        // Возможные причины: неверная мнемоника, неверный пароль БД,
        // контрольная сумма BIP-39 не совпала
        log(`Ошибка восстановления: ${e}`, 'error');
    }
}
