// ─── Argon2id ────────────────────────────────────────────────────────────────

/// Pepper для Argon2id — фиксированный секрет уровня приложения.
///
/// Добавляется к паролю перед деривацией ключа.
/// Защищает от атак на заранее вычисленные таблицы (rainbow tables)
/// даже если соль утекла.
pub const ARGON2_PEPPER: &[u8] = b"ROOT_v2_PEPPER_2026";

/// Объём памяти для Argon2id в килобайтах (64 MB).
///
/// Чем больше — тем дороже перебор паролей для атакующего.
pub const ARGON2_MEMORY_KB: u32 = 65536;

/// Количество итераций Argon2id.
///
/// Увеличивает время вычисления без роста памяти.
pub const ARGON2_ITERATIONS: u32 = 3;

/// Степень параллелизма Argon2id.
///
/// 1 = однопоточно. Для мобильных устройств оптимально.
pub const ARGON2_PARALLELISM: u32 = 1;

// ─── BIP39 ───────────────────────────────────────────────────────────────────

/// Количество слов в мнемонической фразе.
///
/// 24 слова = 256 бит энтропии — максимальная надёжность BIP39.
/// Меньше (12 слов) допустимо но не рекомендуется для мессенджера
/// с фокусом на приватность.
pub const MNEMONIC_WORD_COUNT: usize = 24;

/// Размер seed-байт получаемых из мнемоники (в байтах).
///
/// BIP39 всегда возвращает 64 байта из `mnemonic.to_seed()`.
/// Первые 32 байта → X25519 (ECDH шифрование).
/// Вторые 32 байта → Ed25519 (подписи сообщений).
pub const SEED_BYTES: usize = 64;

/// Смещение в seed для X25519 ключа.
///
/// Байты 0..32 из BIP39 seed.
pub const SEED_X25519_OFFSET: usize = 0;

/// Смещение в seed для Ed25519 ключа.
///
/// Байты 32..64 из BIP39 seed.
pub const SEED_ED25519_OFFSET: usize = 32;
